export function getArticleBySlug(slug) {
  return articles.find((a) => a.slug === slug) ?? null;
}

export function getHeroArticles() {
  const marked = articles.filter((a) => a.hero);
  return marked.length ? marked : articles.slice(0, 4);
}

export function getEditorialArticles() {
  const hero = getHeroArticles();
  const heroSlugs = new Set(hero.map((a) => a.slug));
  return articles.filter((a) => !heroSlugs.has(a.slug)).slice(0, 4);
}
